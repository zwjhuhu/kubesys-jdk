public class SetAddressTest {
	
	
	public static void main(String[] args) throws Exception {

		ExtendedKubernetesClient client = AbstractTest.getClient();
		boolean successful = client.virtualMachineNetworks()
				.modifyAddress("wyw123", get());
		System.out.println(successful);
	}

	protected static ModifyAddress get() {
		ModifyAddress setAddress = new ModifyAddress();
		setAddress.setAddress("123.123.1.1,123.123.1.2");
		return setAddress;
	}
}
